    <h2>Visi, Misi, dan Tujuan</h2>
    
    <h3>Visi</h3>
    <p>Menjadi Rumah Sakit Hewan Pendidikan terkemuka di tingkat nasional dan internasional dalam pemberian
        pelayanan paripurna, pendidikan, dan penelitian di bidang kesehatan hewan, yang unggul dan mandiri serta
        bermartabat berdasarkan moral, agama, etika dengan tetap berorientasi pada kesejahteraan masyarakat.</p>
    
    <h3>Misi</h3>
    <ol>
        <li>Menyelenggarakan fungsi pelayanan terintegrasi, bermutu dan mengutamakan keselamatan dan kesehatan
            pasien (klien).</li>
        <li>Menyelenggarakan pendidikan dan pelatihan terintegrasi bidang kedokteran hewan dan kesehatan lainnya
            untuk menghasilkan lulusan atau tenaga kesehatan yang kompeten di bidangnya.</li>
        <li> Melakukan penelitian terintegrasi berbasis pada keunggulan bidang kedokteran hewan dan kesehatan
            lainnya yang berorientasi pada produk inovasi.</li>
        <li> Menjadi pusat rujukan pelayanan kedokteran hewan dan kesehatan lain yang unggul.</li>
        <li> Mengembangkan manajemen rumah sakit hewan yang produktif, efisien, bermutu, dan berbasis kinerja.</li>
    </ol>
    
    <h3>Tujuan</h3>
    <ul>
        <li>Menjadi Rumah Sakit Hewan yang adaptif, kreatif dan proaktif terhadap tuntutan perkembangan ilmu pengetahuan
        dan teknologi di bidang pengobatan kesehtan hewan</li>
        <li>Menjadi Rumah Sakit Hewan mandiri yang bertatakelola baik</li>
    </ul>
